                <div class="contentat"><p align="center">
	【杏福硒峪】这里有一套高端时尚的绿色健康秘笈<br />
最近，豆瓣高评分神剧《我不是药神》，火的一塌糊涂，上映至今13天，票房已达25.59亿。 <br />
&nbsp;许多人看过电影后，都能感觉到人在面对病魔时的无奈，这也反应出了当下人们越来越重视的一个问题：健康！<br />
习近平总书记指出：“我们既要绿水青山，也要金山银山。宁要绿水青山，不要金山银山，而且绿水青山就是金山银山。”<br />
&nbsp;绿色与健康，才是当今人们生活所追求的高端时尚。<br />
&nbsp;但如今，及其不规律的作息时间、暴饮暴食、长期接受手机等电子产品的辐射……使得无数人的健康受到了严重影响，甚至于患上“癌”等不治之症！<br />
&nbsp;那么，你想要健康吗？<br />
&nbsp;河北杏福硒（xi）峪（yu）生态农业开发有限公司，将为您送上一份高端时尚的健康秘笈！<br />
&nbsp;位于太行山东麓的河北涉县，人杰地灵、风景秀丽。在这个美丽的地方，孕育着一种自然之瑰宝，健康之灵药——杏。<br />
&nbsp;杏在中草药中具有重要地位，主治风寒肺病，生津止渴，润肺化痰，清热解毒等功效。<br />
&nbsp;而在近现代科学的研究中，杏更是被发现含有丰富的微量元素——硒。<br />
&nbsp;杏福硒峪通过多次考察，接受健康专家的指导意见，在当地政府的大力支持下，决心构建一个由“杏”堆砌的“硒”峪。让“硒”之光照亮亿万人的健康之路。<br />
&nbsp;目前，杏福硒峪投入了大量资金与科研力量，在当地政府的监督指导下，积极进行“杏”“硒”类产品开发<br />
&nbsp;并将立足于当地杏树种植基地，大力开发生态旅游，让客户、游客赏杏花、吃杏果、品杏酒、嗑杏仁、种杏树。让所有追求健康的人，感受到杏所带来的幸福。
